CREATE TABLE Ciudad (
Nombre_Ciudad varchar (30) not null,
Cod_ciudad int not null,
Ubicacion varchar (30) not null,
primary key(Cod_ciudad)
);

CREATE TABLE Gimnasio (
Cod_ciudad int not null,
Cod_gim int not null,
Nombre_gimnasio varchar (30) not null,
Tipo_Gim varchar (30) not null,
Nombre_Lider varchar(30) not null,
Nombre_Medalla varchar(30) not null,
foreign key(Cod_ciudad) references Ciudad (Cod_ciudad)
);

CREATE TABLE CentroPokemon (
Cod_Centro int not null,
Cod_ciudad int not null,
Nombre_Enfermera varchar (30) not null,
Rut_Enfermera varchar(30),
primary key(Cod_Centro),
foreign key (Cod_ciudad) references Ciudad (Cod_ciudad)
);


CREATE TABLE Tienda (
Cod_ciudad int not null,
Cod_objeto int not null,
Nombre_tienda varchar (30) not null,
Tipo_tienda varchar(30) not null,
Stock int not null,
foreign key (Cod_ciudad) references Ciudad (Cod_ciudad)
);

CREATE TABLE Pokemon (
Cod_centro int not null,
Cod_pokemon int not null,
Nombre_Pokemon varchar(30) not null,
Tipo_primario varchar (30) not null,
Tipo_secundario varchar(30),
Hp_Pokemon int not null,
Ataque_Pokemon int not null,
Defensa_Pokemon int not null,
Tipo_Ataque varchar(30),
foreign key (Cod_centro) references CentroPokemon (Cod_Centro)
);


